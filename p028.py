#                        P28: Number spiral diagonals                          #
################################################################################
#                                                                              #
#   What is the sum of the numbers on the diagonals in a 1001 by 1001 spiral?  #
#                                                                              #
################################################################################
#                     Problem found at projecteuler.net                        #
#                              Author: ncfgrill                                #
################################################################################

s, c = 1, 2

for n in range(3, 1003, 2):
    s += n**2 + (n**2 - c) + ((n-1)**2 + 1) + (n**2 - c*3)
    c += 2

print('Sum:', s)
