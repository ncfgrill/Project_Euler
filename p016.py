#                                                                              #
#              What is the sum of the digits of the number 2^1000?             #
#                                                                              #
################################################################################
#                     Problem found at projecteuler.net                        #
#                              Author: ncfgrill                                #
################################################################################

d, s = str(2**1000), 0
for i in range(len(d)):
  s += int(d[i])

print('Sum:', s)
