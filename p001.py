################################################################################
#                         P1: Multiples of 3 and 5                             #
################################################################################
#                                                                              #
#           Find the sum of all the multiples of 3 or 5 below 1000.            #
#                                                                              #
################################################################################
#                     Problem found at projecteuler.net                        #
#                              Author: ncfgrill                                #
################################################################################

s = 0

for i in range(3, 1000):
  if i % 3 == 0 or i % 5 == 0:
    s += i

print('Sum:', s)
