################################################################################
#                               P48: Self powers                               #
################################################################################
#                                                                              #
#  Find the last ten digits of the series, 1^1 + 2^2 + 3^3 + ... + 1000^1000.  #
#                                                                              #
################################################################################
#                     Problem found at projecteuler.net                        #
#                              Author: ncfgrill                                #
################################################################################

n, s = 1, 0

while n <= 1000:
  s += n**n
  n += 1

print('Last 10 digits:', s % 10000000000)
